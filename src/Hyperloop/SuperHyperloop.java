public class SuperHyperloop {
    private int vida;
    private int ataque;
    private int tiempo;

    public SuperHyperloop() {
    }

    public int getAtaque() {
        return ataque;
    }

    public int getTiempo() {
        return tiempo;
    }

    public int getVida() {
        return vida;
    }

    public void setAtaque(int ataque) {
        this.ataque = ataque;
    }

    public void setTiempo(int tiempo) {
        this.tiempo = tiempo;
    }

    public void setVida(int vida) {
        this.vida = vida;
    }
    
    
    
}
